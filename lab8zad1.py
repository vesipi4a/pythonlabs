import math # Добавяне на външен модул към кода на програмата
def calc(x,y,z): # Функция за изчисление на лицето на триъгълника
    p=(x+y+z)/2
    s=math.sqrt(p*(p-x)*(p-y)*(p-z))
    return s # Връщане на резултат
def calc2(x,y,z):
    P =x+y+z
    return P
