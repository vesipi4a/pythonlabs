if (a+b<=c) or (a+c<=b) or (b+c<=a): # Проверка за съществуване на триъгълника
    print("Не съществува такъв триъгълник!")
else:
    rez=calc(a,b,c) # Викане на функция
    s="%.2f"% (rez)
    print(f"S = {s}") # Извеждане на резултат
